void Enter_Standby_Mode() {
	//Enter standby mode
	/*clear the WU flag   */
	__HAL_PWR_CLEAR_FLAG(PWR_FLAG_WU);

	//logging the message
	size = sprintf(msg, "Low power standby Mode is On \n\r");
	HAL_UART_Transmit(&huart1, (uint8_t*) msg, size, 1000);

	//finally enter standby mode
	HAL_PWR_EnterSTANDBYMode();
}

